    private static readonly FrozenSet<string> ReservedHandleSkeletons = ReservedHandles
        .Select(RemoveSeparators)
        .ToFrozenSet(StringComparer.OrdinalIgnoreCase);

    [GeneratedRegex(ProfileRules.DetailedHandlePattern, RegexOptions.Compiled)]
    private static partial Regex PatternRegex();

    public static string Normalize(string? handle) => handle?.Trim().ToLowerInvariant() ?? "";

    public static HandleUnavailableReason? GetFormatError(string? handle)
    {
        if (string.IsNullOrWhiteSpace(handle) || handle.Length < ProfileRules.MinLength)
            return HandleUnavailableReason.TooShort;

        if (handle.Length > ProfileRules.MaxLength)
            return HandleUnavailableReason.TooLong;

        if (!PatternRegex().IsMatch(handle))
            return HandleUnavailableReason.InvalidFormat;

        return IsReserved(handle) ? HandleUnavailableReason.Reserved : null;
    }

    private static bool IsReserved(string handle) =>
        ReservedHandles.Contains(handle)
        || ReservedHandleSkeletons.Contains(RemoveSeparators(handle));

    private static string RemoveSeparators(string handle) =>
        handle.Replace("-", string.Empty).Replace("_", string.Empty);
